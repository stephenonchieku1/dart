void main(List<String> args) {
  var num1 = 15;
  var num2 = 20;

  print(num1 > num2);
  print(num1 < num2);
  print(num1 >= num2);
  print(num1 <= num2);
  var age1 = 17;
  var age2 = 10;
  print(age1 == age2);
  print(age1 != age2);
  // equality operators can be used on non-intergrals
  //such as strings
  var name = "steve";
  var dad = "sam";
  print(name == dad);
  print(name != dad);
}
